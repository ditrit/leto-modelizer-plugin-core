/**
 * Class that contains the default configuration for Monaco Editor.
 */
class DefaultConfiguration {
  /**
   * Default constructor.
   */
  constructor() {
    /**
     * No editor.
     */
    this.editor = null;
    this.register = {};
    this.name = "";
    this.config = {};
    this.provider = {};
  }
}

export default DefaultConfiguration;
